// Logika Operator
// Operator Logika adalah operator yang digunakan untuk membandingkan dua buah nilai boolean. Operator logika terdiri dari AND (&&), OR (||), dan NOT (!). Operator logika digunakan untuk menggabungkan dua buah nilai boolean dan menghasilkan nilai boolean baru.
// 1. Operator AND (&&)

let age = 18;
let gender = "male";

if (gender === "male" && age === 18) {
  console.log("Kamu adalah laki-laki dewasa");
} else {
  console.log("Kamu bukan laki-laki dewasa");
}

// 2. Operator OR (||)
let cuaca = "hujan";
let barang = "payung";

if (cuaca === "cerah" || barang === "motor") {
  console.log("Kamu membutuhkan payung");
} else {
  console.log("Kamu tidak membutuhkan payung");
}

